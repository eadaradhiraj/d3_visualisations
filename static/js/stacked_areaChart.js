function render_line_chart(config) {
  var margin = config.margin;
  var data = config.data;
  data = _.sortBy(data, function (d) {
    return d[config.sortby_column];
  });

  var width = $(config.selector).width() - margin.left - margin.right,
    height = 500 - margin.top - margin.bottom;

  var svg = d3
    .select(config.selector)
    .append("svg")
    .attr("width", width + margin.left + margin.right)
    .attr("height", height + margin.top + margin.bottom);

  var tooltip = d3.select("body").append("div").attr("class", "toolTip");

  var g = svg
    .append("g")
    .attr("transform", "translate(" + margin.left + "," + margin.top + ")");

  if (config.sortby_column.toLowerCase().startsWith("date") ||
    config.sortby_column.toLowerCase() == "time") {
    var x = d3.scaleTime().rangeRound([0, width]);
  } else {
    var x = d3.scaleLinear().rangeRound([0, width]);
  }

  var y = d3.scaleLinear().rangeRound([height, 0]);

  var area = d3
    .area()
    .x(function (d) {
      return x(d.data[config.sortby_column]);
    })
    .y0(function (d) {
      return y(d[0]);
    })
    .y1(function (d) {
      return y(d[1]);
    });

  var stack = d3.stack();
  var keys = _.reject(Object.keys(data[0]), function (x) {
    return x == "total" || x == "Dated" || x == "date" || x == "time";
  });

  x.domain(
    d3.extent(data, function (d) {
      return d[config.sortby_column];
    })
  );

  // Find the value of the day with highest total value
  var maxDateVal = d3.max(data, function (d) {
    var vals = d3.keys(d).map(function (key) {
      if (key !== "date" && key !== "total")
        return d[key]
      else
        return 0
    });
    return d3.sum(vals);
  });

  y.domain([0, maxDateVal]);
  stack.keys(keys);
  var layer = g
    .selectAll(".layer")
    .data(stack(data))
    .enter()
    .append("g")
    .attr("class", "layer");
  
  var color = d3.scaleOrdinal(d3.schemeCategory20);

  layer
    .append("path")
    .attr("class", "area")
    .style("fill", function(d) { return color(d.key); })
    .attr("d", area)
    .on("mousemove", function (data) {
      var x0 = x.invert(d3.mouse(this)[0])

      filtered_data = data.filter(function (d) {
        var x0_date = new Date(Number(x0))
        var search_date = new Date(Number(d.data[config.sortby_column]))
        if (x0_date.getMonth() == search_date.getMonth() &&
          x0_date.getDay() == search_date.getDay() &&
          x0_date.getFullYear() == search_date.getFullYear())
          return true
        else
          return false
      })[0]

      var toolTip_html = ''
      $.each(filtered_data.data, function (index, value) {
        if (!index.toLowerCase().startsWith("date") &&
          index.toLowerCase() !== "time") {
          toolTip_html += index + ":" + value + "<br>";
        }
      })

      tooltip
        .style("left", d3.event.pageX - config.tooltip.left + "px")
        .style("top", d3.event.pageY - config.tooltip.left + "px")
        .style("display", "inline-block")
        .html(toolTip_html);
    })
    .on("mouseout", function (d) {
      tooltip.style("display", "none");
    });

  g
    .append("g")
    .attr("class", "axis axis--x")
    .attr("transform", "translate(0," + height + ")")
    .call(d3.axisBottom(x).tickFormat(d3.timeFormat(config.time_format)))
    .selectAll("text")
    .attr("dx", config.xaxis.label_dx)
    .attr("dy", config.xaxis.label_dy)
    .attr("transform", config.xaxis.label_transform);

  g
    .append("g")
    .attr("class", "axis axis-y")
    .call(d3.axisLeft(y));
}
