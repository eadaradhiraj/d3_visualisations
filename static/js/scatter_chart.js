function scatter_chart(config) {
    var data = config.data,
        x = config.x,
        y = config.y,
        r = config.r;
    var margin = config.margin;
    var width = 960 - margin.left - margin.right;
    var height = 500 - margin.top - margin.bottom;

    var svg = d3.select('#scatter_chart1')
        .append('svg')
        .attr('width', width + margin.left + margin.right)
        .attr('height', height + margin.top + margin.bottom)
        .append('g')
        .attr('transform', 'translate(' + margin.left + ',' + margin.top + ')');

    var xScale = d3.scaleLinear()
        .range([0, width]);

    var yScale = d3.scaleLinear()
        .range([height, 0]);

    // square root scale.
    var radius = d3.scaleSqrt()
        .range([2, 5]);

    var xAxis = d3.axisBottom()
        .scale(xScale);

    var yAxis = d3.axisLeft()
        .scale(yScale);

    // adding arbitrary colours
    var color = d3.scaleOrdinal(d3.schemeCategory20);

    xScale.domain(d3.extent(data, function (d) {
        // return d.SepalLength;
        return d[x];
    })).nice();

    yScale.domain(d3.extent(data, function (d) {
        // return d.SepalWidth;
        return d[y];
    })).nice();

    radius.domain(d3.extent(data, function (d) {
        // return d.PetalLength;
        return d[r];
    })).nice();

    // translate x-axis to (0,height) and it's defined to be a bottom axis. 
    svg.append('g')
        .attr('transform', 'translate(0,' + height + ')')
        .attr('class', 'x axis'+" "+config.selector)
        .call(xAxis);

    // y-axis is translated to (0,0)
    svg.append('g')
        .attr('transform', 'translate(0,0)')
        .attr('class', 'y axis'+" "+config.selector)
        .call(yAxis);


    var bubble = svg.selectAll('.bubble'+"."+config.selector)
        .data(data)
        .enter().append('circle')
        .attr('class', 'bubble'+" "+config.selector)
        .attr('cx', function (d) {
            return xScale(d[x]);
        })
        .attr('cy', function (d) {
            return yScale(d[y]);
        })
        .attr('r', function (d) {
            return radius(d[r]);
        })
        .style('fill', function (d) {
            return color(d[config.key]);
        });

    bubble.append('title')
        .attr('x', function (d) {
            return radius(d[r]);
        })
        .text(function (d) {
            return d[config.key];
        });

    // adding label. For x-axis, it's at (10, 10), and for y-axis at (width, height-10).
    svg.append('text')
        .attr('x', 10)
        .attr('y', 10)
        .attr('class', 'label'+" "+config.selector)
        .text(y);

    svg.append('text')
        .attr('x', width)
        .attr('y', height - 10)
        .attr('text-anchor', 'end')
        .attr('class', 'label'+" "+config.selector)
        .text(x);

    // define a group element for each color i, and translate it to (0, i * 20). 
    var legend = svg.selectAll('legend'+"."+config.selector)
        .data(color.domain())
        .enter().append('g')
        .attr('class', 'legend'+" "+config.selector)
        .attr('transform', function (d, i) {
            return 'translate(0,' + i * 20 + ')';
        });

    // give x value equal to the legend elements. 
    // no need to define a function for fill, this is automatically fill by color.
    legend.append('rect')
        .attr('x', width)
        .attr('width', 18)
        .attr('height', 18)
        .style('fill', color);

    // add text to the legend elements.
    // rects are defined at x value equal to width, we define text at width - 6, this will print name of the legends before the rects.
    legend.append('text')
        .attr('x', width - 6)
        .attr('y', 9)
        .attr('dy', '.35em')
        .style('text-anchor', 'end')
        .text(function (d) {
            return d;
        });


    // d3 has a filter fnction similar to filter function in JS. Here it is used to filter d3 components.
    legend.on('click', function (type) {
        d3.selectAll('.bubble'+"."+config.selector)
            .style('opacity', 0.15)
            .filter(function (d) {
                return d[config.key] == type;
            })
            .style('opacity', 1);
    })
}

